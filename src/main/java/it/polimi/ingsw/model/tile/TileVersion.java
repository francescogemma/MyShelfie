package it.polimi.ingsw.model.tile;

/**
 * Indicates the version of a {@link Tile} which corresponds to its foreground image.
 *
 * @author Cristiano Migali
 */
public enum TileVersion {
    /**
     * It is the first version of a tile.
     */
    FIRST,

    /**
     * It is the second version of a tile.
     */
    SECOND,

    /**
     * It is the third version of a tile.
     */
    THIRD
}
